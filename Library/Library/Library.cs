using System;
namespace Library
{
    public class Library
    {
        public static void math()
        {
                public int Sum(int a, int b)
                {
                    return a + b;
                }
                public int Subtraction(int a, int b)
                {
                    return a - b;
                }
                public int Multiply(int a, int b)
                {
                    return a * b;
                }
                public int Divide(int a, int b)
                {
                    return a / b;
                }
                public int Mod(int a, int b)
                {
                    return a % b;
                }
         }
     }
}
